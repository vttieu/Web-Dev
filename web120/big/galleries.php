            Your image gallery can be very straightforward where you click on an image currently on the screen and it will enlarge that image and allow you traverse through the available              images. You can do so by clicking the left and right arrow keys on the screen or using your keyboard’s left and right arrow keys. 
            CSS based galleries are very appealing to the eye as you can create a lot of moving parts. A more ideal choice for a fancy and clean site that just shows competence. Images                can be highlighted by adjusting their brightness and shape.
