 <h2 class="pageID">Parallax Research</h2>
    <p>The parallax effect or parallax scrolling is generally moving background or images to create an visual depth to simulate a third dimension on a screen. 
    It could be very subtle such the background automatically zooming in or out from your mouse hovering over it. 
    Or a two layer background where one moves as you scroll and the other doesn't. 
    Or a page where you instead of scrolling you drag the screen around (plus one for interactivity with users)</p>
    <p>There are already many famous brands implementing this on their website. It can sometime be the decisive factor of whether or not a
    random user will buy your product. This is especially beneficial for businesses that are selling innovative products. I think the Tesla site is the perfect
    example of this effect. As you design your cars you can numbers on the screen moving. This is of course all for user experience which increases their time spent
    on your website and this is exactly what you would want.</p>
    <p>I think it also shows how much you believe in your own product with the amount of creativity you put in designing your site</p>
    <p>Parallax can be implemented in a variety of ways according to <a href="https://envato.com/blog/parallax-scrolling-in-web-design/" target="_blank">Envato</a>
    <ul>
        <li>Raster Parallax, which is mostly used to give an optical illusion of movement.</li>
        <li>Repeat Pattern, that uses scrolling displays over a repeated background.</li>
        <li>Layered Parallax, where the foreground and background objects move at a different pace.</li>
        <li>Sprite Method, scrolling through an image you designed.</li>
    </ul>
    </p>
    <p>This will look good to any employer on a portfolio!</p>
