 <h2 class="pageID">Responsive Site v.s. Native App.</h2>
    <p>According to <a href="https://www.stonetemple.com/mobile-vs-desktop-usage-study/" target="_blank">Stone Temple</a>
    <ul>
        <li>In 2018, 58% of site visits were from mobile devices.</li>
        <li>Mobile devices made up 42% of total time spent online.</li>
        <li>Mobile Bounce Rate came in at 50%.</li>
    </ul>
    </p>
    <p>With the amount of traffic a site gets from mobile devices, a responsive mobile page is a must for any company looking to expand their brand. A mobile app is usually the step after       that depending on the needs/business of the company.
    </p>
    <p>Responsive mobile refers to the separate url of a webpage for when you’re browsing on your phone. These websites mostly starts with an ‘m’. (ex. “m.facebook.com”). Although thesse      url are meant to be used on your mobile phone, you can still access it through your desktop browser. 
    </p>
    <p>Pros
        <ul>
            <li>Compared to a mobile app, this is cheaper and faster to launch by a far margin.</li>
            <li>Loads faster when surfing the web on your browser (This is based on personal use, I would not exit my browser just to open another app when I can access the same               information through their website whether they have a mobile specific page or not.)</li>
            <li>Available for all users, without the need to update and take up space in your device’s memory.</li>
        </ul>
    </p>
    <p>Cons
        <ul>
            <li>Takes longer to navigate the site ( This is true in most cases but I suspect that it might be due to the time and money companies are willing to pour into their app vs their mobile site.</li>
        </ul>
    </p>
    <p>Mobile apps refers to the application that you would download from stock apps like “play store” on android or “app store” on IOS. Just like that messenger app that facebook try to force everyone to use. 
    </p>
    <p>Pros
        <ul>
            <li>There are so much more features you can implement through a mobile app than a regular boring website whose main purpose is to just display content.</li>
            <li>More ways to interact with an app than the limitations of an html page.</li>
            <li>People will find it easier to be attached to an app they can interact with increasing your brand recognition.</li>
            <li>Built with efficiency in mind, creates a snappy feel when tapping through the contents.</li>
            <li>Having the option to go offline.</li>
            <li>Allow users to customize and/or change the general appearance of the app.</li>
            <li>Notifications, quick access to personal account, and more ways to get into your account other than typing in a username and password.</li>
            <li>Leverage on your device capabilities like accessing the camera, GPS, heartrate monitor, etc..</li>
        </ul>
    </p>
    <p>Cons
        <ul>
            <li>Expensive and requires a longer time to put together.</li>
            <li>It’s not easy to perfect the looks and in app experience. So it requires a bigger investment from companies.</li>
        </ul>
    </p>

